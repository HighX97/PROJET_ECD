
public class Our_occurence 
{
	private int value;
	private int max;
	
	Our_occurence(int value, int max)
	{
		this.setValue(value);
		this.setMax(max);
	}

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
